CREATE TABLE `customer` (
  `CUST_ID` varchar(5) NOT NULL,
  `CUST_NAME` varchar(64) NOT NULL,
  `CUST_ADDRESS` varchar(96) NOT NULL,
  `CUST_PHONENUMBER` varchar(10) NOT NULL,
  `CUST_DEBITNUMBER` varchar(15) NOT NULL,
  `CUST_DEBITBALANCE` decimal(7,2) NOT NULL,
  PRIMARY KEY (`CUST_ID`),
  UNIQUE KEY `CST_DEBITNUM` (`CUST_DEBITNUMBER`)
) ENGINE=MEMORY DEFAULT CHARSET=utf8	

CREATE TABLE `financialhistory` (
  `FIN_ID` varchar(5) NOT NULL,
  `FIN_CUSTID` varchar(5) NOT NULL,
  `FIN_TRANSACTONAMNT` decimal(7,2) NOT NULL,
  `FIN_DATE` date NOT NULL,
  PRIMARY KEY (`FIN_ID`)
) ENGINE=MEMORY DEFAULT CHARSET=utf8	

CREATE TABLE `reservations` (
  `RES_ID` varchar(5) NOT NULL,
