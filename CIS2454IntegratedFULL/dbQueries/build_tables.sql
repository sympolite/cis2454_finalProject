  `RES_STARTDATE` date NOT NULL,
  `RES_ENDDATE` date NOT NULL,
  `RES_CUSTID` varchar(5) NOT NULL,
  PRIMARY KEY (`RES_ID`)
) ENGINE=MEMORY DEFAULT CHARSET=utf8	

